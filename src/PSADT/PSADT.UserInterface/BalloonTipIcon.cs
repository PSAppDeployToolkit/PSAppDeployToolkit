namespace PSADT.UserInterface
{
    /// <summary>
    /// Defines a set of standardized icons that can be associated with a balloon tip.
    /// </summary>
    public enum BalloonTipIcon
    {
        /// <summary>
        /// Not a standard icon.
        /// </summary>
        None = 0,

        /// <summary>
        /// An information icon.
        /// </summary>
        Info = 1,

        /// <summary>
        /// A warning icon.
        /// </summary>
        Warning = 2,

        /// <summary>
        /// An error icon.
        /// </summary>
        Error = 3,
    }
}
