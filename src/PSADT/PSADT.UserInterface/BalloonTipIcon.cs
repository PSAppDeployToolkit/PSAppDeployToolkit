namespace PSADT.UserInterface
{
    /// <summary>
    /// Defines a set of standardized icons that can be associated with a balloon tip.
    /// </summary>
    public enum BalloonTipIcon
    {
        /// <summary>
        /// Not a standard icon.
        /// </summary>
        None,

        /// <summary>
        /// An information icon.
        /// </summary>
        Info,

        /// <summary>
        /// A warning icon.
        /// </summary>
        Warning,

        /// <summary>
        /// An error icon.
        /// </summary>
        Error
    }
}
